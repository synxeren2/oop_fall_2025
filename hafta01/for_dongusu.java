package hafta01;

public class for_dongusu {
    public static void main(String[] args) {
        int i;
        for (i=0;i<100;i++){
            System.out.print(i+" - ");
        }//for bloğu bitişi

        System.out.println();
        System.out.println();
        for (i=100;i<300;i+=5){
            System.out.print(i+" - ");
        }//for bloğu bitişi


        System.out.println();
        System.out.println();
        for (i=100;i>0;i--){
            System.out.print(i+" - ");
        }//for bloğu bitişi



    }//main method bitişi
} //class bitişi
